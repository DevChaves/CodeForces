#Escreva um programa que leia 2 (dois) números inteiros e mostre o produto dos mesmos.

#Input
#A entrada contém duas linhas, cada uma contendo um número inteiro, a e b ( - 100000 ≤ a, b ≤ 100000).

#Output
#A saída deve conter uma única linha o resultado do produto a × b.

a = int(input())
b = int(input())
produto = a * b
print(produto)